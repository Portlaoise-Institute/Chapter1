package com.mycompany.chapter1;

public class DataTypes {
 
    public static void main(String[] args) {
        
        //Declare and init 5 variables
        char letter = 'M';
        String title = "Hello";
        int number = 365;
        float decimal = 98.6f;  //f suffix 
        boolean result = true;
        
        //Output variables
        // +  concatenate operator - join strings together 
        System.out.println("* Outputting variables *");
        System.out.println("Initial letter: "+letter);
        System.out.println("Title: "+title);
        System.out.println("Days are: "+number);
        System.out.println("Temp is: "+ decimal);
        System.out.println("Result is: "+result);
        
    }//end main
    
} //end class
